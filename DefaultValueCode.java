package SangamOneJavaCode;

public class DefaultValueCode {

	byte bvalue;
	short svalue;
	int intvalue;
	long lvalue;
	float fvalue;
	double dvalue;
	char cvalue;
	boolean bovalue;
	Object objectValue;
	
	public static void main(String[] args) {
		DefaultValueCode dob = new DefaultValueCode();
		System.out.println("Default values:");
		System.out.println("byte:" +dob.bvalue);
		System.out.println("short:" +dob.svalue);
		System.out.println("int:" +dob.intvalue);
		System.out.println("long:" +dob.lvalue);
		System.out.println("float:" +dob.fvalue);
		System.out.println("double:" +dob.dvalue);
		System.out.println("char:" +dob.cvalue);
		System.out.println("boolean:" +dob.bovalue);
		System.out.println("object:" +dob.objectValue);
		
		
		
		
		
		
		

	}

}
